
# get degree measure in Fahrenheit
measure_in_Fahrenheit = float(input("Enter degree measure in Fahrenheit: "))

# compute the degree measure in Fahrenheit to celsius degree measure.
Fahrenheit_in_celsius = 33.8
calculated_measure_in_celsius = (measure_in_Fahrenheit * Fahrenheit_in_celsius)

# output the calculated degree measure in celsius to the UserWarning
print("The result in celsius degree measure is "+ str(calculated_measure_in_celsius))
